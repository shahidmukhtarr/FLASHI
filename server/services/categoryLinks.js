export function getRelevantCategoryUrls(query) {
  if (!query || !query.trim()) return [];

  const normalizedQuery = query.toLowerCase();
  const queryWords = normalizedQuery.split(/[^a-z0-9]+/).filter(Boolean);

  const matchedUrls = CATEGORY_PAGE_CONFIG.filter((page) => {
    const haystack = [page.title.toLowerCase(), page.store.toLowerCase(), ...(page.keywords || [])].join(' ');
    return queryWords.some((word) => haystack.includes(word));
  }).map((page) => page.url);

  return Array.from(new Set(matchedUrls));
}
